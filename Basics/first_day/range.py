'''
INTRODUCTION
Syntax:
range(start,stop,step)

for loop with range 
Ex: for i in range(5):
0,1,2,3,4

for loop with range(start,stop)
ex: for i in range(1,6):
1,2,3,4,5


for loop with range(start,stop,step)
ex: for i in range(1,10,2):
1,3,5,7,9

range() only works with integer arguments.
all three argument can be positive or negative
step value should not be 0
'''